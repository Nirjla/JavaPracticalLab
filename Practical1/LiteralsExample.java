public class LiteralsExample {
 public static void main(String[] args) {
    System.out.println("Floating Literals:");
    float normal = 2.555f;
    float exponential = 1.2E5f;
    System.out.println("Float in standard format: "+normal);
    System.out.println("Float in scientific format: "+exponential);
    System.out.println("Boolean Literals:");
    boolean trueLiteral = true;
    boolean falseLiteral = false;
    System.out.println("For True condition: "+trueLiteral);
    System.out.println("For False condition: "+falseLiteral);
    System.out.println("Character Literals:");
    char charLiteral = 'N';
    char escapeSequence = '\n';
    System.out.println("Character: "+charLiteral);
    System.out.println("Escape Sequence: "+escapeSequence);
    System.out.println("String Literals:");
    String myString = "This is my first java program";
    String escapeString = "Hello \'World\'";
    System.out.println(myString);
    System.out.println(escapeString);


 
}   
}
